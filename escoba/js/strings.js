// NOT translated, on purpose (see HANDOFF-I18N-EXTRACTION.md decision 7 + escoba's own rules):
// - Card-suit terms Oros/Copas/Espadas/Bastos, and named figures Sota/Caballo/Rey -- the games'
//   real vocabulary in BOTH languages.
// - "Escoba"/"Escobas"/"¡ESCOBA!" -- the game's own name and its central move, kept identical
//   in both dictionaries deliberately (same idea as the game title itself).
// - `deck.js`'s cardLabel() output (e.g. "7 de Oros") -- a pure data-layer formatter, out of
//   scope for this UI-only pass, and already Spanish-phrased regardless of hub language.
// - Round-modal bonus footnotes ("Opponent under 10 cards", "All the coins", "All four 7s") --
//   these strings are generated by escoba/js/game.js (the engine, out of scope; see root
//   CLAUDE.md "Adding a game" and the i18n handoff's "never touch normDiff/recorders/engines"
//   boundary), so they stay English even with the hub set to Spanish. A future pass would need
//   to move them into a lookup keyed by item.key rather than baking the label into the engine.
// - Storage vocabulary: difficulty ids (easy/normal/hard), deckMode ids (spanish/american), mode
//   ids (solo/host/join), and the 'version' MP-error sentinel all stay canonical -- only their
//   display labels translate.

